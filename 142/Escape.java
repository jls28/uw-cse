// Prints out an inspirational Wayne Gretzky quote by Michael Scott.
// Formatted as in this picture:
// http://www.cs.washington.edu/education/courses/cse142/12su/lectures/06-18/quote.jpg
public class Escape {
	public static void main(String[] args) {
		System.out.println("\"You miss 100% of the");
		System.out.println("shots you don't");
		System.out.print("\t");
		System.out.println("take -Wayne Gretzky\"");
		System.out.print("\t\t");
		System.out.println("-Michael Scott");
	}
}